// Re-initialize when content changes
const observer = new MutationObserver((mutations) => {
    for (const mutation of mutations) {
        const blogSectionAdded = Array.from(mutation.addedNodes).some(node => 
            node.nodeType === 1 && (
                node.classList?.contains('blog-section') ||
                node.querySelector?.('.blog-section')
            )
        );
        
        if (blogSectionAdded) {
            console.log('Blog section detected in DOM changes, re-initializing...');
            initializeBlog();
            break;
        }
    }
});

// Start observing the content div for changes
console.log('Setting up mutation observer');
const contentDiv = document.getElementById('content');
if (contentDiv) {
    observer.observe(contentDiv, { childList: true, subtree: true });
}